#integer

#addition
base_leaves = 14;
role_leaves = 70;
total = role_leaves  + base_leaves;
print(f"Both are adding: {total}");

#multiplication
people = 4;
salary = 45000;
total_expense = people * salary;
print(f"Total Expense:  {total_expense}");

#divisor
total_tea_bags = 7;
pots = 4;
bags_per_pots = total_tea_bags / pots;
print(f"Total Tea Bags: {bags_per_pots}");

#divisor without float 
total_tea_bags = 7;
pots = 4;
bags_per_pots = total_tea_bags // pots;
print(f"Total Tea Bags: {bags_per_pots}");

# modulo which represent the remaining left 
total_cad = 10;
pots = 4;
left_pots = total_cad % pots;
print(f"Total Tea Bags: {left_pots}");

